export async function GET(request) {
  const { searchParams } = new URL(request.url);
  const country = searchParams.get('country');
  const state = searchParams.get('state');
  const city = searchParams.get('city');

  if (city && state && country) {
    // Return zip codes for specific city
    const countryData = SHIPPING_DATA[country.toUpperCase()];
    if (!countryData || !countryData.states[state.toUpperCase()]) {
      return Response.json({ error: 'Location not supported for shipping' }, { status: 404 });
    }
    const stateData = countryData.states[state.toUpperCase()];
    const zipCodes = stateData.cities[city] || [];
    return Response.json({ zipCodes });
  }

  if (state && country) {
    // Return cities for specific state
    const countryData = SHIPPING_DATA[country.toUpperCase()];
    if (!countryData || !countryData.states[state.toUpperCase()]) {
      return Response.json({ error: 'Location not supported for shipping' }, { status: 404 });
    }
    const stateData = countryData.states[state.toUpperCase()];
    const cities = Object.keys(stateData.cities || {});
    return Response.json({ cities });
  }

  if (country) {
    // Return states for specific country
    const countryData = SHIPPING_DATA[country.toUpperCase()];
    if (!countryData) { 
      return Response.json({ error: 'Country not supported for shipping' }, { status: 404 });
    }
    const states = Object.entries(countryData.states).reduce((acc, [code, data]) => {
      acc[code] = typeof data === 'string' ? data : data.name;
      return acc;
    }, {});
    return Response.json({ states });
  }

  // Return all supported countries
  const countries = Object.entries(SHIPPING_DATA).map(([code, data]) => ({
    code,
    name: data.name
  }));

  return Response.json({ countries });
}